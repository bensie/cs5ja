import javax.swing.*;
import java.awt.*;
import java.text.*;

public class FlyingShapes {

  public static void main (String[] args) {

  int r, d, b, x, y, w, h, shape;  

/////////////////////////////////////////////////////////////////////////////////////////////////////////
//Create JFrame Window:
/////////////////////////////////////////////////////////////////////////////////////////////////////////

  JFrame window;
     Container contentPane;
     Graphics g;

     window = new JFrame();
     window.setSize(1000,600);
     window.setVisible(true);
     window.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

     contentPane=window.getContentPane();
     g = contentPane.getGraphics();

/////////////////////////////////////////////////////////////////////////////////////////////////////////
//Pick Random Colors:
/////////////////////////////////////////////////////////////////////////////////////////////////////////

  int count = 0;
    while (count >= 0) {

      r = (int) (Math.random()*256);
      d = (int) (Math.random()*256);
      b = (int) (Math.random()*256);

/////////////////////////////////////////////////////////////////////////////////////////////////////////
//Pick Random Locations:
/////////////////////////////////////////////////////////////////////////////////////////////////////////

      x = (1 + (int)(Math.random()*1000));
      y = (1 + (int)(Math.random()*600));
      w = (1 + (int)(Math.random()*500));
      h = (1 + (int)(Math.random()*500));

/////////////////////////////////////////////////////////////////////////////////////////////////////////
//Pick Random Shape and Draw:
/////////////////////////////////////////////////////////////////////////////////////////////////////////

      shape = (1 + (int)(Math.random()*3));

      if (shape == 1) {
        g.setColor(new Color (r, d, b));
        g.fillOval(x, y, w, h);
        }

      if (shape == 2) {
        g.setColor(new Color (r, d, b));
        g.fillRect(x, y, w, h);
        }

      if (shape == 3) {
        g.setColor(new Color (r, d, b));
        g.drawLine(x, y, w, h);
        } 
      
/////////////////////////////////////////////////////////////////////////////////////////////////////////
//Busy Wait:
/////////////////////////////////////////////////////////////////////////////////////////////////////////

      for (int ctr = 0; ctr < 1000000; ctr++) {
      }
      
      count++;

    }

  }
}
